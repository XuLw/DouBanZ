package com.zjs.example.doubanz.bean;

public class Avatars {
    private String small;


    public void setSmall(String small) {
        this.small = small;
    }

    public String getSmall() {
        return this.small;
    }

    @Override
    public String toString() {
        return "Avatars{" +
                "small='" + small + '\'' +
                '}';
    }
}
